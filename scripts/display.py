def display_costopt_graph(mw_generated: list[int], plants: list[PowerPlant]):
    # from dr. logan's graph, x is plant names (with bar width mw drawn) and y is plant cost
    if mw_generated.any():  # returns false if nothing in array, true if at least one item
        names = [p.name for p in plants]
        plant_costs = [p.plant_cost for p in plants]

        sorted_list: list[str, int, float] = []
        for i in range(len(plants)):
            if (mw_generated[i] != 0):
                sorted_list.append([names[i], mw_generated[i], plant_costs[i]])
        sorted_list = np.array(sorted_list)
        sorted_list = sorted_list[sorted_list[:,2].argsort()]

        names = [p[0] for p in sorted_list]
        mw_generated = [int(p[1]) for p in sorted_list]
        plant_costs = [float(p[2]) for p in sorted_list]

        colors = []
        for i in range(len(mw_generated)):
            colors.append(list(np.random.random(size=3)))
            # TODO make colors mean something? idk if i can here but we'll see

        # resizes bars to fit together nicely. adapted from https://stackoverflow.com/questions/70477458/how-can-i-plot-bar-plots-with-variable-widths-but-without-gaps-in-python-and-ad
        x_marks = []
        for i in range(len(mw_generated)):
            x_marks.append(sum(mw_generated[:i]) + (mw_generated[i]/2))

        # create graph
        graph = plt.bar(x_marks, height=plant_costs, width=mw_generated, color=colors)

        # graph/axis titles
        plt.title('mW Generated by Plant')
        plt.xlabel('Plant (width is amt of mW)')
        plt.ylabel('Plant Cost')

        # limits
        plt.xlim(left=0, right=sum(mw_generated))
        plt.ylim(bottom=0, top=((9/8) * max(plant_costs)))

        # misc
        plt.axhline(y=max(plant_costs), color='black', linestyle='-')  # horiz line to show wholesale cost/profit margins
        plt.legend(graph.patches, names, loc=4)
        
        # show graph
        plt.show()
    
    else: popup('Cannot graph results, no best solution was found.')


def display_generation_graph(history, rows=1, cols=1, plot_num=1, **kwargs):  # graph cost per generation. REQUIRED TO RUN ON MAIN THREAD
    # axis values
    xvals = [run.n_gen for run in history]
    yvals = [run.pop.get("F").min() for run in history]
    gvals = [run.pop.get("G").sum() for run in history]

    colors = []
    for cvsum in gvals:
        if cvsum > 0: colors.append('red')
        else: colors.append('green')

    plt.subplot(rows, cols, plot_num)
    plt.bar(xvals, yvals, color=colors)

    # graph/axis titles
    # plt.xlabel('Generation')
    # plt.ylabel('Cost (e10^6)')
    plt.title(f'{max(xvals)} Gens, F={int(yvals[max(xvals) - 1])}')

    if 'dontplotyet' not in kwargs:
        plt.show()


def display_diffnum_generation_graph(histories):
    num_graphs = len(histories)
    rows = int(math.sqrt(num_graphs)) + 1
    cols = int(num_graphs / rows) + 1

    for i, history in enumerate(histories):
        display_generation_graph(history, rows, cols, i+1, dontplotyet=True)

    # plt.tight_layout(pad=0.5)
    plt.subplots_adjust(left=0.05, right=0.95, 
                        top=0.9, bottom=0.0, 
                        wspace=0.2, hspace=0.55)
    plt.suptitle('Effects of Differing Numbers of Generations (x = Gens, y = Cost 1e6)')
    plt.show()


# only show paretofront
def display_pareto(result, best_dot, extremes):
    # adapted from https://pymoo.org/algorithms/moo/nsga2.html
    plt = Scatter(labels=['Cost ($million)', 'Water (L/kWh)', 'Carbon (L/kWh?)'])

    # add pareto front
    for dot in result:
        plt.add(dot, color='royalblue', alpha=0.25, s=10)

    for ex_dot in extremes:
        plt.add(ex_dot, color='limegreen', edgecolors='black', s=30)

    # add best option based on user's weightings
    plt.add(best_dot, color='crimson', marker='*', edgecolors='black', s=150)
    
    plt.show()


def rotate_that_cube(result, best_dot, extremes):  # note that this saves both a png and a gif to the gifs folder
    degrees = 360
    step = 2

    fig = plt.figure()
    ax = fig.add_subplot(projection='3d')

    # # Set the axis labels
    ax.set_xlabel('Cost ($million)')
    ax.set_ylabel('Water (L/kWh)')
    ax.set_zlabel('Carbon (L/kWh?)')

    # add pareto front
    xs = []
    ys = []
    zs = []
    for dot in result:
        xs.append(float(dot[0]))
        ys.append(float(dot[1]))
        zs.append(float(dot[2]))
    ax.scatter3D(xs, ys, zs, color='blue', alpha=0.25, s=10)

    # add extreme values
    exs = []
    eys = []
    ezs = []
    for ex_dot in extremes:
        exs.append(float(ex_dot[0]))
        eys.append(float(ex_dot[1]))
        ezs.append(float(ex_dot[2]))
    ax.scatter3D(exs, eys, ezs, color='limegreen', edgecolors='black', s=30)

    # add best option based on user's weightings
    ax.scatter3D(best_dot[0], best_dot[1], best_dot[2], color='crimson', marker='*', edgecolors='black', s=150)

    progress_bar = alive_bar(
        total=int(degrees/step), 
        title='Collecting frames...',
        theme='smooth'
    )

    folderpath = 'gifs/_gifimages'
    resultpath = 'gifs/pareto' + str(int(time.time()))
    images = []
    to_remove = []

    if os.path.exists('gifs') == False: os.mkdir('gifs')
    if os.path.exists(folderpath) == False: os.mkdir(folderpath)

    # save a png figure
    print(f'Saving figure...')
    ax.view_init(45, 45)
    plt.savefig(resultpath + '.png', dpi=300)
    print(f'Figure saved at {resultpath}.png.')

    # collect images every two degrees to be combined into a gif
    with progress_bar as bar:
        for angle in range(0, degrees, step):
            # Normalize the angle to the range [-180, 180] for display (from https://matplotlib.org/stable/gallery/mplot3d/rotate_axes3d_sgskip.html)
            angle_norm = (angle + 180) % 360 - 180

            # Update the axis view and title
            ax.view_init(15, angle_norm)  # a higher first value will tilt the cube more towards you
            
            filepath = folderpath + '/' + str(angle) + '.png'
            plt.savefig(filepath)
            images.append(imageio.imread(filepath))
            to_remove.append(filepath)
            bar()

    # generate the gif from the images and save it
    print('Generating GIF...')
    resultpath += '.gif'
    imageio.mimsave(resultpath, images, duration=10, loop=0)

    # delete temporary images/folders. leaves only the generated gif/png
    print('GIF generated. Cleaning up...')
    for file in to_remove:
        os.remove(file)
    os.chmod(folderpath, 664) 
    os.rmdir(folderpath)

    print(f'Done. GIF is located at {resultpath}.')

